function Ploss=Bearcal(N,D,u,Q,type)
% ผฦหใึแณะห๐บฤนฆยส
%ืชหูฃฌDึฑพถฃฌuถฏมฆีณถศฃฌqปฌำอม๗มฟฃฌtypeึแณะภเะอ
switch type
    case 0
        Ploss=0;
    case 1
        %น๖ึ้ึแณะ
        Ploss=4.87e-15*D^3.95*N^1.75*u^0.4*1000+3.19e-10*N*D*Q*1000;
    case 2   
        %น๖ึ๙ึแณะ
        Ploss=2.07e-15*D^3.95*N^1.75*u^0.4*1000+1.56e-10*N*D*Q*1000;
    case 3   
        %าบันพถฯ๒ึแณะ
        Ploss=2.48e-14*D^3.95*N^1.75*u^0.4*1000+1.87e-9*N*D*Q*1000;
    case 4   
        %าบันึนอฦึแณะ
        Ploss=5.84e-14*D^3.95*N^1.75*u^0.4*1000+3.83e-9*N*D*Q*1000;
end
end